    private boolean isAllValid() {
        String username = txtUsername.getText();
        String password = txtPass.getText();
        String soDu = txtSoDu.getText();
        String phoneNum = txtSdt.getText();
        String hoTen = txtHoten.getText();
        String email = txtEmail.getText();
        if (username.isBlank() == true || password.isBlank() == true || soDu.isBlank() == true || phoneNum.isBlank() == true || hoTen.isBlank() == true || email.isBlank() == true) {
            return false;
        } else {
            return true;
        }
    }

    private void loadComboChoice(){
        KhuVucDAO khuVucDAO = new KhuVucDAO();
        List<String> lstKhuVuc = new ArrayList<>();
        lstKhuVuc.clear();
        cboKhuvuc.removeAllItems();
        lstKhuVuc = khuVucDAO.getListKhuVuc();
        for (String string : lstKhuVuc) {
            cboKhuvuc.addItem(string);
        }
    }
    private TaiKhoan getInfo() {
        String username = txtUsername.getText();
        String password = txtPass.getText();
        int soDu = Integer.parseInt(txtSoDu.getText());
        String phoneNum = txtSdt.getText();
        String hoTen = txtHoten.getText();
        String email = txtEmail.getText();
        boolean vaiTro = String.valueOf(cboRole.getSelectedItem()).equals("Admin") ? true : false;
        return new TaiKhoan(vaiTro, username, password, soDu, phoneNum, email, hoTen);
    }

    private void changeColor() {
        Timer timer = new Timer(1000, e -> {
            Random rand = new Random();
            Color randomColor = new Color(rand.nextInt(256), rand.nextInt(256), rand.nextInt(256));
            lblXinChao.setForeground(randomColor);
        });
        timer.start();
    }

    private void updateTable() {
        Timer timer = new Timer(3500, e -> {
            this.load2Table();
            this.loadThongKeTaiKhoan();
            this.loadDoanhThuMon();
            this.loadComboChoice(); 
        });
        timer.start();
    }

    private void load2Table() {
        Tmodel = (DefaultTableModel) tblUser.getModel();
        Tmodel.setRowCount(0);
        List<TaiKhoan> lstTk = new ArrayList<>();
        lstTk.clear();
        TaiKhoanDAO tkDAO = new TaiKhoanDAO();
        lstTk = tkDAO.getData();
        for (TaiKhoan taiKhoan : lstTk) {
            Object[] tk_add = new Object[]{tkDAO.getSTT(taiKhoan.getSdt()), taiKhoan.isVai_tro() ? "Admin" : "Khách hàng", taiKhoan.getTen_dang_nhap(), taiKhoan.getMat_khau(), taiKhoan.getSo_du(), taiKhoan.getSdt(), taiKhoan.getHo_ten(), taiKhoan.getEmail()};
            Tmodel.addRow(tk_add);
        }
        tblUser.setModel(Tmodel);
    }

    public void loadThongKeTaiKhoan() {
        DefaultTableModel tabThongKe = (DefaultTableModel) this.TabThong_ke_tai_khoan.getModel();
        tabThongKe.setRowCount(0);
        ThongKeTaiKhoanDAO TKTKDAO = new ThongKeTaiKhoanDAO();
        List<ThongKeTaiKhoan> TKTKLst = TKTKDAO.ThongTinThongKeTK();
        for (ThongKeTaiKhoan ThongKe : TKTKLst) {
            tabThongKe.addRow(new Object[]{
                ThongKe.getId_tk(),
                ThongKe.getHo_ten(),
                ThongKe.getSo_du()
            });
        }
    }

    public void loadDoanhThuMon() {
        DefaultTableModel tabDoanhThu = (DefaultTableModel) this.tabDoanhThuMon.getModel();
        tabDoanhThu.setRowCount(0);
        ThongKeDonHangDAO TKDHdao = new ThongKeDonHangDAO();
        List<ThongKeDonHang> TKDHLst = TKDHdao.ThongTinThongKeDonHang();
        for (ThongKeDonHang DoanhThu : TKDHLst) {
            tabDoanhThu.addRow(new Object[]{
                DoanhThu.getId_san_pham(),
                DoanhThu.getTen_sp(),
                DoanhThu.getSo_luong_ban(),
                DoanhThu.getTong_doanh_thu()
            });
        }
    }

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String sdt = txtSdt.getText();
        int check = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa ?");
        if (check == JOptionPane.YES_OPTION) {
            TaiKhoanDAO tkDAO = new TaiKhoanDAO();
            tkDAO.deleteAccount(sdt);
            JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thao tác bị hủy bởi người dùng");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int check = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn thêm tài khoản không ?");
        if (check == JOptionPane.YES_OPTION) {
            boolean validResult = this.isAllValid();
            if (validResult == true) {
                try (Connection conn = KetNoiDB.getConnect()) {
                    TaiKhoan tkAdd = this.getInfo();
                    TaiKhoanDAO tkDAO = new TaiKhoanDAO();
                    tkDAO.addAccount(tkAdd);
                    JOptionPane.showMessageDialog(rootPane, "Thêm tài khoản thành công");
                    this.load2Table();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "An error has occurred: " + e);
                    e.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng không bỏ trống các ô !");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thao tác bị hủy bởi người dùng");
        }
    }//GEN-LAST:event_btnThemActionPerformed


