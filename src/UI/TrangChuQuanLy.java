    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        boolean validResult = this.isAllValid();
        if (validResult == true) {
            try (Connection conn = KetNoiDB.getConnect()) {
                TaiKhoan tkAdd = this.getInfo();
                TaiKhoanDAO tkDAO = new TaiKhoanDAO();
                tkDAO.addAccount(tkAdd);
                JOptionPane.showMessageDialog(rootPane, "Thêm tài khoản thành công");
                this.load2Table();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "An error has occurred: " + e);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng không bỏ trống các ô !");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        // TODO add your handling code here:
        int row = tblUser.getSelectedRow();
        if (row != -1) {
            txtUsername.setText(tblUser.getValueAt(row, 2).toString());
            txtPass.setText(tblUser.getValueAt(row, 3).toString());
            txtSoDu.setText(tblUser.getValueAt(row, 4).toString());
            txtSdt.setText(tblUser.getValueAt(row, 5).toString());
            txtHoten.setText(tblUser.getValueAt(row, 6).toString());
            txtEmail.setText(tblUser.getValueAt(row, 7).toString());
            
            String VT = tblUser.getValueAt(row, 1).toString();
            cboRole.setSelectedItem(VT);
        }
    }//GEN-LAST:event_tblUserMouseClicked
    
