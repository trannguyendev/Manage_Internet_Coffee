    private void displayQR(String amount) throws IOException {
        String vietqrAPI = "https://img.vietqr.io/image/mbbank-0867514526-compact.png?amount="+amount+"&addInfo=Nap%20tien%20tai%20khoan%20"+GlobalState.ten_dang_nhap;
        try {
        // Fetch QR code image
        BufferedImage qrCode = ImageIO.read(new URL(vietqrAPI));
        if (qrCode == null) {
            throw new IOException("Failed to load QR code image from API.");
        }

        //Start NapTheDAO
            NapTheDAO napDAO = new NapTheDAO();
            
        // Display QR code in Swing UI
        JPanel buttonPanel = new JPanel(); // Default FlowLayout
        JButton payNowButton = new JButton("Paycheck");
        JButton cancelButton = new JButton("Cancel");
        payNowButton.setForeground(Color.pink);
        cancelButton.setForeground(Color.pink);
        payNowButton.setFont(new Font("Monospace", Font.BOLD, 19));
        cancelButton.setFont(new Font("Monospace", Font.BOLD, 19));
        payNowButton.addActionListener(e -> {
            napDAO.AddAccount(GlobalState.menh_gia, GlobalState.ten_dang_nhap, 0);
        });
        
        buttonPanel.add(payNowButton);
        buttonPanel.add(cancelButton);

        // Add the button panel to the bottom of the main panel

        JFrame frame = new JFrame("QR Payment");
        cancelButton.addActionListener(e -> {
           frame.dispose();
        });
        frame.setLocationRelativeTo(null);
        frame.add(buttonPanel, BorderLayout.SOUTH);
        JLabel label = new JLabel(new ImageIcon(qrCode));
        frame.add(label);
        frame.pack();
        frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE); // Keeps app running
        frame.setVisible(true);

    } catch (IOException e) {
        // Error feedback
        System.err.println("Error generating QR code: " + e.getMessage());
        e.printStackTrace();
    }


    }

